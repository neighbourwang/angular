    validate(name: string, val: any, op: string) {
        let map: any = {
            "*": {
                "func": this.validationService.isBlank,
                "msg": "NET_MNG_VM_IP_MNG.CANT_NULL"
            },
             "url": {
                "func": val => !this.isURL(val),
                "msg": "NET_MNG_VM_IP_MNG.INVALID"
            }
        }

        if (map[op].func(val)) {
            return [name, map[op].msg];
        }
        else
            return undefined;
    }



